diadasemana =  int(input('Qual dia da semana você quer saber?'))

if diadasemana==1:
    print('Domingo')
elif diadasemana==2:
    print('Segunda')
elif diadasemana==3:
    print('Terça')
elif diadasemana==4:
    print('Quarta')
elif diadasemana==5:
    print('Quinta')
elif diadasemana==6:
    print('Sexta')
elif diadasemana==7:
    print('Sábado')
else:
    print('Número inválido para essa pergunta.')
